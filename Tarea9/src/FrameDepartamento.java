    private void jTextFieldCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldCodigoFocusLost

        // TODO add your handling code here:
        boolean a = jTextFieldCodigo.getText().chars().allMatch(Character::isDigit);
        if (!jTextFieldCodigo.getText().isEmpty() && a)
        {
            String DatosConsulta[]= new String[3];
            DepartamentoMetodos depmetodos= new DepartamentoMetodos();
            DatosConsulta =depmetodos.MostrarDatosEnJtext(Integer.parseInt(jTextFieldCodigo.getText()));
            jTextFieldNombre.setText(DatosConsulta[0]);
            jTextFieldLocalizacion.setText(DatosConsulta[1]);
            jTextFieldManager.setText(DatosConsulta[2]);
        }
    }//GEN-LAST:event_jTextFieldCodigoFocusLost
