    private void jButtonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBorrarActionPerformed
        jTextArea1.setText("");
      
        boolean a = jTextFieldCodigo.getText().chars().allMatch(Character::isDigit);
        if (jTextFieldCodigo.getText().isEmpty()) {
           
            JOptionPane.showMessageDialog(null, "Introduce algún código", "ERROR", JOptionPane.WARNING_MESSAGE);
            
        } else if(a) {
            DepartamentoMetodos depm = new DepartamentoMetodos();
            if(depm.BorrarDatos(Integer.parseInt(jTextFieldCodigo.getText()))>0)
            {
                jTextArea1.setText("Borrado completado del codigo: "+jTextFieldCodigo.getText()+" completado");
                jTextFieldCodigo.setText("");
            }
            else
            {
               jTextArea1.setText("No se borro el codigo de departament : "+jTextFieldCodigo.getText()); 
            }
            
            
        }
        else
            {
             
             JOptionPane.showMessageDialog(null, "El codigo debe ser numerico", "ERROR", JOptionPane.WARNING_MESSAGE);
            }
        resetTexto();
                   
    }//GEN-LAST:event_jButtonBorrarActionPerformed

