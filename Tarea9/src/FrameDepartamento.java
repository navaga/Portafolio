    private void jButtonInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertarActionPerformed
        jTextArea1.setText("");
        
        
        boolean a = jTextFieldCodigo.getText().chars().allMatch(Character::isDigit); // comprueba si lo que hay escrito en un campo es un digito(numero) devuelve bool
        boolean b = jTextFieldLocalizacion.getText().chars().allMatch(Character::isDigit);//comprueba si lo que hay escrito en un campo es un digito(numero) devuelve bool
        boolean c= jTextFieldManager.getText().chars().allMatch(Character::isDigit);
        if (a && b && c) {
            if (jTextFieldLocalizacion.getText().isEmpty() || jTextFieldCodigo.getText().isEmpty() || jTextFieldManager.getText().isEmpty() || jTextFieldNombre.getText().isEmpty()) {
                //jTextArea1.setText("Hay que rellenar todos los campos");
                
                JOptionPane.showMessageDialog(null, "Hay que rellenar todos los campos", "ERROR", JOptionPane.WARNING_MESSAGE);
            } else {
                
            Departamento dep = new Departamento(jTextFieldNombre.getText(),Integer.parseInt(jTextFieldCodigo.getText()),Integer.parseInt(jTextFieldLocalizacion.getText()),Integer.parseInt(jTextFieldManager.getText()));
            DepartamentoMetodos depmetodos= new DepartamentoMetodos();
            if(depmetodos.InsertarDatos(dep)>0)
            {
              jTextArea1.setText(depmetodos.Mostar());
              jTextArea1.append("DATOS INTRODUCIDOS CORRECTAMENTE");
            }
            else
                jTextArea1.setText("EL CODIGO YA EXISTE");
            
                
                
                
                
                
                resetTexto();
            }
        } else {
            
            JOptionPane.showMessageDialog(null, "los campos codigo,localizacion y manager deben ser numericos", "ERROR", JOptionPane.WARNING_MESSAGE);
        } 

    }//GEN-LAST:event_jButtonInsertarActionPerformed

